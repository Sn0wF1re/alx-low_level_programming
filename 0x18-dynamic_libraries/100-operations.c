/**
 * add - return sum of two numbers
 * @x: integer
 * @y: integer
 *
 * Return: sum
 */
int add(int x, int y)
{
	return (x + y);
}

/**
 * sub - return difference of 2 integers
 * @x: integer
 * @y: integer
 *
 * Return: difference
 */
int sub(int x, int y)
{
	return (x - y);
}

/**
 * mul - return product
 * @x: integer
 * @y: integer
 *
 * Return: product
 */
int mul(int x, int y)
{
	return (x * y);
}

/**
 * div - return result of division
 * @x: dividend
 * @y: divisor
 *
 * Return: quotient
 */
int div(int x, int y)
{
	return (x / y);
}
/**
 * mod - return remainder of division
 * @x: dividend
 * @y: divisor
 *
 * Return: remainder
 */
int mod(int x, int y)
{
	return (x % y);
}
